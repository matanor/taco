function plotParamsEffect ...
        ( numMistakes, sorted, sortOrder, title, figurePath )

t = ['Effect of params on ' title];
f = figure('name',t);
plotIndex.rows = 4;
plotIndex.cols = 2;
plotIndex.current = 1;
plotIndex = ...
    plotNumMistakes( numMistakes(sortOrder.K),...
                    sorted.K, 'K', title, plotIndex);
plotIndex = ...
    plotNumMistakes( numMistakes(sortOrder.alpha),...
                    sorted.alpha, 'alpha', title, plotIndex);
plotIndex = ...
    plotNumMistakes( numMistakes(sortOrder.beta),...
                    sorted.beta, 'beta', title, plotIndex);
plotIndex = ...
    plotNumMistakes( numMistakes(sortOrder.labeledConfidence),...
                    sorted.labeledConfidence, 'gamma', ...
                    title, plotIndex);
              
saveas(f, figurePath);
close(f);

end