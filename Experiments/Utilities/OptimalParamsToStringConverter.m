classdef OptimalParamsToStringConverter
    
methods (Static)
    function R = convert( optimal, algorithm_i, EMPTY_CELL, SEPERATOR)
        if algorithm_i == SingleRun.CSSLMC || ...
           algorithm_i == SingleRun.CSSLMCF
           R = [num2str(optimal.values.alpha) SEPERATOR ... 
                num2str(optimal.values.beta)  SEPERATOR ...
                num2str(optimal.values.labeledConfidence) SEPERATOR ... 
                num2str(optimal.values.K)     SEPERATOR ...
                EMPTY_CELL EMPTY_CELL EMPTY_CELL ...
                EMPTY_CELL EMPTY_CELL EMPTY_CELL ];
        elseif algorithm_i == SingleRun.MAD
           R = [EMPTY_CELL EMPTY_CELL EMPTY_CELL ...
                num2str(optimal.values.K)   SEPERATOR ... 
                num2str(optimal.values.mu1) SEPERATOR ...
                num2str(optimal.values.mu2) SEPERATOR ... 
                num2str(optimal.values.mu3) SEPERATOR ...
                EMPTY_CELL EMPTY_CELL EMPTY_CELL ];
        elseif algorithm_i == SingleRun.AM
           R = [EMPTY_CELL EMPTY_CELL EMPTY_CELL ...
                num2str(optimal.values.K)   SEPERATOR ... 
                EMPTY_CELL EMPTY_CELL EMPTY_CELL ... 
                num2str(optimal.values.am_v)     SEPERATOR ...
                num2str(optimal.values.am_mu)    SEPERATOR ...
                num2str(optimal.values.am_alpha) SEPERATOR ];
        end
    end
end

end

